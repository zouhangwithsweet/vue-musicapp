<template>
    <scroll class="suggest" 
            :data="result" 
            ref="scroll" 
            :pullup="pullup"
            :beforeScroll="beforeScroll"
            @beforeScroll="listScroll"
            @scrollToEnd="searchMore">
        <ul class="suggest-list">
            <li @click="selectItem(item)" class="suggest-item" v-for="item in result" :key="item">
                <div class="icon">
                    <i :class="getIconCls(item)"></i>
                </div>
                <div class="name">
                    <p class="text" v-html="getDisplayName(item)">
                    </p>
                </div>
            </li>
            <loading v-show="hasMore"></loading>
        </ul>
        <div class="no-result-wrapper" v-show="!hasMore && !result.length">
            <no-result title="无搜索结果"></no-result>
        </div>
    </scroll>
</template>

<script>
    import {search} from 'api/search'
    import {ERR_OK} from 'api/config'
    import {createSong} from 'common/js/song'
    import Scroll from 'base/scroll/scroll'
    import loading from 'base/loading/loading'
    import Singer from 'common/js/singer'
    import {mapMutations, mapActions} from 'vuex'
    import noResult from 'base/no-result/no-result'

    const TYPE_SINGER = 'singer'
    const perpage = 20
    export default {
        props: {
            query: {
                type: String,
                default: ''
            },
            showSinger: {
                type: Boolean,
                default: true
            }
        },
        data() {
            return {
                page: 1,
                result: [],
                pullup: true,
                beforeScroll: true,
                hasMore: true
            }
        },
        methods: {
            search() {
                this.page = 1
                this.hasMore = true
                this.$refs.scroll.scrollTo(0, 0)
                search(this.query, this.page, this.showSinger, perpage).then(res => {
                    if (res.code === ERR_OK) {
                        this.result = this._genResult(res.data)
                        this._checkMore(res.data)
                    }
                })
            },
            searchMore() {
                if (!this.hasMore) {
                    return
                }
                this.page++
                search(this.query, this.page, this.showSinger, perpage).then((res) => {
                    if (res.code === ERR_OK) {
                        this.result =this.result.concat(this._genResult(res.data))
                        this._checkMore(res.data)
                    }
                })
            },
            getIconCls(item) {
                if (item.type === TYPE_SINGER) {
                    return 'icon-mine'
                } else {
                    return 'icon-music'
                }
            },
            getDisplayName(item) {
                if (item.type === TYPE_SINGER) {
                    return item.singername
                } else {
                    return `${item.name}-${item.singer}`
                }
            },
            selectItem(item) {
                if (item.type === TYPE_SINGER) {
                    const singer = new Singer({
                        id: item.singermid,
                        name: item.singername
                    })
                    console.log(singer)
                    this.$router.push({
                        path: `/search/${singer.id}`
                    })
                    this.setSinger(singer)
                } else {
                    this.insertSong(item)
                }
                this.$emit('select')
            },
            listScroll() {
                this.$emit('listScroll')
            },
            refresh() {
                this.$refs.scroll.refresh()
            },
            _checkMore(data) {
                const song = data.song
                if (!song.list.length || (song.curnum + song.curpage) * 20 > song.tatalnum) {
                    this.hasMore = false
                }
            },
            _genResult(data) {
                let ret = []
                if (data.zhida && data.zhida.singerid) {
                    ret.push({...data.zhida, ...{type: TYPE_SINGER}})
                }
                if (data.song) {
                    ret = ret.concat(this._normalizeSons(data.song.list))
                }
                return ret
            },
            _normalizeSons(list) {
                let ret = []
                list.forEach(musicData => {
                    if (musicData.songid && musicData.albumid) {
                        ret.push(createSong(musicData))
                    }
                })
                return ret
            },
            ...mapMutations({
                setSinger: 'SET_SINGER'
            }),
            ...mapActions([
                'insertSong'
            ])
        },
        watch: {
            query() {
                this.search()
            }
        },
        components: {
            Scroll,
            loading,
            noResult
        }
    }
</script>

